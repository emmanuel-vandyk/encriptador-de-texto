/* Funcion encriptadora */
function encriptarTexto() {
    var x = '';

    switch(x) {
        case 'a':
            console.log('enter');
        case 'e':
            console.log('ai');
        case 'i':
            console.log('imes');
        case 'o':
            console.log('ober');
        case 'u':
            console.log('ufat');
        break;
    }
}